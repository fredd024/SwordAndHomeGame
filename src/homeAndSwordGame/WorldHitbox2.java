    private static ArrayList<Blockade> blocakdeArray = new ArrayList<>();

    public static void loadMapHitbox() {
        int x = 0;
        int y = 0;

        for (int i = 0; i < hitboxGrid.length; i++) {
            if (hitboxGrid[i] == 1){
                Blockade blockade = new Blockade();
                blockade.setDiemsion(16,16);
                blockade.teleport(x * 16, y * 16);
                CollidableRepository.getInstance().registerEntity(blockade);
                blocakdeArray.add(blockade);
            }
            if (x == 59){
                x = 0;
                y++;
            }else {
                x++;
            }

        }
    }

    public static void draw(Canvas canvas){
        for (int i = 0; i < blocakdeArray.size(); i++) {
            blocakdeArray.get(i).draw(canvas);
        }
    }
